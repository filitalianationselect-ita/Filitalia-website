function formatLongTextHTML(text){
  const raw = String(text || "").replace(/\r\n/g,"\n").replace(/\r/g,"\n").trim();
  if(!raw) return "";

  // If the text already has line breaks, respect them.
  // If it is one long block, create natural paragraphs after sentence endings.
  let chunks = raw.includes("\n")
    ? raw.split(/\n+/)
    : raw
        .replace(/\s+/g," ")
        .replace(/([.!?])\s+(?=(?:[A-ZÀ-Ý0-9]|[•-]))/g, "$1\n")
        .replace(/\s+(?=\d+[.)]\s)/g, "\n")
        .split(/\n+/);

  chunks = chunks
    .map(part => part.trim())
    .filter(Boolean);

  return chunks.map(part => {
    const numbered = /^\d+[.)]\s*/.test(part);
    const bullet = /^[•-]\s*/.test(part);
    const className = numbered || bullet ? "info-paragraph info-point" : "info-paragraph";
    return `<p class="${className}">${safe(part)}</p>`;
  }).join("");
}

