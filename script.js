function getEventTimestamp(item){
  if(!item) return Number.MAX_SAFE_INTEGER;

  const raw = item.sortDate || item.eventDate || item.dateISO || item.date;

  if(typeof raw === "object" && raw !== null){
    return getEventTimestamp({ sortDate: raw.it || raw.en || raw.ph || Object.values(raw)[0] });
  }

  if(!raw) return Number.MAX_SAFE_INTEGER;

  const value = String(raw).trim();

  // Best format: YYYY-MM-DD. This avoids Safari/browser date confusion.
  const isoMatch = value.match(/^(\d{4})-(\d{2})-(\d{2})/);
  if(isoMatch){
    const y = Number(isoMatch[1]);
    const m = Number(isoMatch[2]) - 1;
    const d = Number(isoMatch[3]);
    return new Date(y, m, d, 23, 59, 59).getTime();
  }

  const parsed = new Date(value).getTime();
  return Number.isNaN(parsed) ? Number.MAX_SAFE_INTEGER : parsed;
}

